    /////////////////////////////////////////////////////////////////////////
    //
    // mHal3dfWarp () -
    //! \brief do warp image 
    //
    /////////////////////////////////////////////////////////////////////////   
    virtual MINT32 mHal3dfWarp(MavPipeImageInfo* pParaIn,MUINT32 *MavResult,MUINT8 ImgNum);
   	
    /////////////////////////////////////////////////////////////////////////
    //
    // mHal3dfGetResult () -
    //! \brief check warp image success or not
    //
    /////////////////////////////////////////////////////////////////////////   
