    //
    // mHalMavAddImg () -
    //! \brief mav add image  
    //
    /////////////////////////////////////////////////////////////////////////      
    virtual MINT32 mHal3dfAddImg(MavPipeImageInfo* pParaIn);

    /////////////////////////////////////////////////////////////////////////
    //
    // mHal3dfGetMavResult () -
    //! \brief mav result info  
