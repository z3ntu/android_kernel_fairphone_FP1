    /////////////////////////////////////////////////////////////////////////
    //
    // mHalMavMerge () -
    //! \brief mav merge image and return result
    //
    /////////////////////////////////////////////////////////////////////////   
    virtual MINT32 mHal3dfDoMotion(void* InputData,MUINT32* MotionResult);
    	    
    /////////////////////////////////////////////////////////////////////////
    //
    // mHalMavWarp () -
    //! \brief do warp image 
    //
    /////////////////////////////////////////////////////////////////////////   
